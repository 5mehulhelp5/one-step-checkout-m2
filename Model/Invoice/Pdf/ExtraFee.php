namespace Mavenbird\OneStepCheckout\Model\Invoice\Pdf;

use Magento\Sales\Model\Order\Pdf\Total\DefaultTotal;

class Extrafee extends DefaultTotal
{
    /**
     * @var \Mavenbird\OneStepCheckout\Helper\Data
     */
    protected $oscHelper;

    /**
     * Construct
     *
     * @param \Magento\Tax\Helper\Data $taxHelper
     * @param \Magento\Tax\Model\Calculation $taxCalculation
     * @param \Magento\Tax\Model\ResourceModel\Sales\Order\Tax\CollectionFactory $ordersFactory
     * @param \Mavenbird\OneStepCheckout\Helper\Data $oscHelper
     * @param array $data
     */
    public function __construct(
        \Magento\Tax\Helper\Data $taxHelper,
        \Magento\Tax\Model\Calculation $taxCalculation,
        \Magento\Tax\Model\ResourceModel\Sales\Order\Tax\CollectionFactory $ordersFactory,
        \Mavenbird\OneStepCheckout\Helper\Data $oscHelper,
        array $data = []
    ) {
        parent::__construct($taxHelper, $taxCalculation, $ordersFactory, $data);
        $this->oscHelper = $oscHelper;
    }

    /**
     * GetTotalsForDisplay
     *
     * @return void
     */
    public function getTotalsForDisplay()
    {
        $amount = $this->getOrder()->formatPriceTxt($this->getAmount());
        if ($this->getAmountPrefix()) {
            $amount = $this->getAmountPrefix() . $amount;
        }

        $title = __($this->oscHelper->getExtraFeeLabel());
        if ($this->getTitleSourceField()) {
            $label = $title . ' (' . $this->getTitleDescription() . '):';
        } else {
            $label = $title . ':';
        }

        $fontSize = $this->getFontSize() ? $this->getFontSize() : 7;
        $total = ['amount' => $amount, 'label' => $label, 'font_size' => $fontSize];
        return [$total];
    }
}
